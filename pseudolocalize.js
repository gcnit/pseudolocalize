const pseudoLocalize = (text) => {
    let localizedText = '';
    const characters = text.split('');
    characters.forEach(character => {
        const isVowel = vowels.indexOf(character.toLowerCase()) > -1;
        const char = map[character] || character;
        localizedText = localizedText.concat(char.repeat(isVowel + 1));
    });
    return localizedText;
};
